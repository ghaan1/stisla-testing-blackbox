Cypress.Commands.add('typeLogin', (user) => {
    cy.visit("http://127.0.0.1:8000/");
    cy.get('input[name=email]').type(user.email);
    cy.get('input[name=password]').type(user.password);
    cy.get('.btn').click();
  })
